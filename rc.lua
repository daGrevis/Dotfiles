    if screen.count() == 1 or s == 2 then
        right_layout:add(separator)
        right_layout:add(calendar)
        right_layout:add(separator)
        if get_hostname() ~= "rx-wks-44" then
            right_layout:add(volume)
            right_layout:add(separator)
            right_layout:add(bat)
            right_layout:add(separator)
        end
